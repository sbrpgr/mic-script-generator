function main() {
  const files = collectHtmlFiles();
  const changed = [];

  for (const file of files) {
    const original = fs.readFileSync(file, "utf8");
    let next = normalizeTrustCopy(original);
    const content = resolveContent(file, next);
    if (content) {
      next = upsertStaticContent(next, content);
    }
    if (next !== original) {
      fs.writeFileSync(file, next);
      changed.push(path.relative(ROOT, file).replace(/\\/g, "/"));
    }
  }

  console.log(`static content enriched: ${changed.length ? changed.join(", ") : "no changes"}`);
}

function collectHtmlFiles() {
  const files = [];
  walk(ROOT, (file) => {
    if (!file.endsWith(".html")) return;
    if (/\s\(\d+\)\.html$/i.test(path.basename(file))) return;
    files.push(file);
  });
  return files.sort();
}

function walk(dir, onFile) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      if ([".git", ".browser-profile", ".cloudflare-dist", ".cloudflare-dist-smoke", ".wrangler", "node_modules"].includes(entry.name)) {
        continue;
      }
      walk(fullPath, onFile);
    } else {
      onFile(fullPath);
    }
  }
}

function resolveContent(file, html) {
  const relative = path.relative(ROOT, file).replace(/\\/g, "/");
  if (relative === "index.html") return { id: "home", ...HOME_CONTENT };

  const toolMatch = html.match(/<body[^>]+data-tool="([^"]+)"/);
  if (toolMatch) {
    const id = toolMatch[1];
    const content = TOOL_CONTENT[id];
    if (!content) throw new Error(`${relative}: missing TOOL_CONTENT for ${id}`);
    return { id, ...content };
  }

  const categoryMatch = html.match(/<body[^>]+data-category-page="([^"]+)"/);
  if (categoryMatch) {
    const id = categoryMatch[1];
    const content = CATEGORY_CONTENT[id];
    if (!content) throw new Error(`${relative}: missing CATEGORY_CONTENT for ${id}`);
    return { id, ...content };
  }

  return null;
}

function normalizeTrustCopy(html) {
  return html;
}

function upsertStaticContent(html, content) {
  const clean = removeExistingPanel(html);
  const footerPattern = /\n(\s*<footer class="site-footer">)/;
  if (!footerPattern.test(clean)) {
    throw new Error(`Could not find footer insertion point for ${content.id}`);
  }
  return clean.replace(footerPattern, `\n${renderPanel(content)}\n\n$1`);
}

function removeExistingPanel(html) {
  const pattern = new RegExp(`\\n?\\s*${escapeRegExp(START)}[\\s\\S]*?${escapeRegExp(END)}\\s*\\n?`, "g");
  return html.replace(pattern, "\n");
}

function renderPanel(content) {
  const id = `staticContentTitle-${content.id}`;
  return `          ${START}
          <section class="panel static-content-panel" aria-labelledby="${escapeAttr(id)}">
            <details class="static-content-details">
              <summary>
                <div>
                  <p class="eyebrow">${escapeHtml(content.eyebrow)}</p>
                  <h2 id="${escapeAttr(id)}">${escapeHtml(content.heading)}</h2>
                </div>
                <span class="static-content-toggle">자세히 보기</span>
              </summary>
              <article class="static-content-article">
                <div class="static-content-body">
${content.paragraphs.map(renderParagraph).join("\n")}
                </div>
                <aside class="static-content-notes" aria-label="핵심 정리">
                  <h3>이 도구에서 특히 신경 쓴 부분</h3>
                  <ul>
${content.notes.map((note) => `                  <li>${escapeHtml(note)}</li>`).join("\n")}
                  </ul>
                </aside>
              </article>
            </details>
          </section>
          ${END}`;
}

function renderParagraph(text) {
  const sentences = splitSentences(text);
  return `                <p>
${sentences.map((sentence) => `                  <span>${escapeHtml(sentence)}</span>`).join("\n")}
                </p>`;
}

function splitSentences(text) {
  return String(text)
    .split(/(?<=\.)\s+/)
    .map((item) => item.trim())
    .filter(Boolean);
}

function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll("\"", "&quot;")
    .replaceAll("'", "&#39;");
}

function escapeAttr(value) {
  return escapeHtml(value);
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

